  <script>
    $("#formAssistence").on("submit", function(event) {
      event.preventDefault();

      var formData = $(this).serialize();
      $('.rta').html('Enviando mensaje...');

      $.ajax({
          method: "POST",
          url: "send-email.php",
          data: formData
        })
      .done(function( res ) {
        res = JSON.parse(res)
        if (res.thereIsError == false) {
            $('.rta').html('Mensaje enviado con éxito');
            ;
        } else {
            $('.rta').html('Error, intentelo nuevamente');
        }
      })
      .fail(function (res) {
          $('.rta').html('Error de servidor, intentelo nuevamente más tarde');
      });
    });
  </script>

